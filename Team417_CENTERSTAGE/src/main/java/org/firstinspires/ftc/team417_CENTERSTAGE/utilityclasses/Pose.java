package org.firstinspires.ftc.team417_CENTERSTAGE.utilityclasses;

// A utility class to bypass the fact that one can only return a single value

public class Pose {
    public double x;
    public double y;
    public double theta;

    public Pose(double x, double y, double theta) {
        this.x = x;
        this.y = y;
        this.theta = theta;
    }

}
